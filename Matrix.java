//Dieses Java-Programm erstellt mit Hilfe von verschachtelten Schleifen eine 
//Matrix aus 3 * 3 'x' via. der Print- und der Printlnmethode.


public class Matrix {
		
	public static void main(String[] args) {
		for (int i = 1; i < 4; i++) {
			//eine verschachtelte for-Schleife		
					for (int j = 1; j < 4; j++) {
						System.out.print("x ");
					}
					System.out.println();
				}

	}

}
